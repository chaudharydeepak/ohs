package com.dc.ehs.entity;

public class MetaData
{
	int id;
	String type;
	String value;
	
	public int getId ( )
	{
		return id;
	}
	public void setId ( int id )
	{
		this.id = id;
	}
	public String getType ( )
	{
		return type;
	}
	public void setType ( String type )
	{
		this.type = type;
	}
	public String getValue ( )
	{
		return value;
	}
	public void setValue ( String value )
	{
		this.value = value;
	}
}
